local MAP_MOVE_ON  = 1
local MAP_MOVE_OFF = 0

local TILE_SIZE = 32

function GetMapData(x, _y)

    --local y = -1 * _y
    --local col = math.floor(x / TILE_SIZE) + 1

	return MAPS[_y][x]
end

local moveAbleList = {}

for _y = 1, 32 do
	for _x = 1, 32 do
		if GetMapData(_x, _y) == MAP_MOVE_ON then
			table.insert(moveAbleList, {x = _x * 32, y = -_y * 32})
		end
	end
end




Server.GetTopic("RANDOM_ITEM").Add(function()
	local field = unit.field

	local ex = math.random(10, 20)

	for i = 1, ex do
		local monster_id = math.random(79, 87)

		local data = moveAbleList[math.random(1, #moveAbleList)]
		local spawn_x = data.x
		local spawn_y = data.y

		field.SpawnEnemy(monster_id, spawn_x, spawn_y)
	end

end)
